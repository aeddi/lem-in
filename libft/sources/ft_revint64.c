#include <stdint.h>

uint64_t	ft_revint64(uint64_t nb)
{
	uint64_t	rev;

	rev = ((nb >> 56) & 0xff) |
	((nb >> 40) & 0xff00) |
	((nb >> 24) & 0xff0000) |
	((nb >> 8) & 0xff000000) |
	((nb << 8) & 0xff00000000) |
	((nb << 24) & 0xff0000000000) |
	((nb << 40) & 0xff000000000000) |
	((nb << 56) & 0xff00000000000000);
	return (rev);
}
